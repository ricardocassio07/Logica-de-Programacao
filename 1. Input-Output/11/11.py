# 11: Receba o raio de uma circunferência. Calcule e mostre o valor de seu comprimento.
raio = (float(input("Digite o valor do raio da circunferência: ")))
π = (3.14159265359)
comprimento = (2 * raio * π)
print("O comprimento da circuferência com raio de {} u.n. é, aproximadamente, {:.2f} u.n.".format(raio, comprimento))